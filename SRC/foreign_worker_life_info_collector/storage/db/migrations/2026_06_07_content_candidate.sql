UPDATE content.content_candidate
SET category = 'foreign_jobs',
    priority_group = 'PRIMARY',
    updated_at = CURRENT_TIMESTAMP,
    content_updated_at = CURRENT_TIMESTAMP
WHERE source_domain = 'SOCIAL_NEWS'
  AND category IN ('foreign_worker_news', 'jobs', 'foreign_worker_policy');

UPDATE content.content_candidate content_row
SET status = 'ARCHIVED',
    updated_at = CURRENT_TIMESTAMP,
    content_updated_at = CURRENT_TIMESTAMP,
    raw_payload = content_row.raw_payload || jsonb_build_object('archive_reason', 'non_representative_or_duplicate_social_news')
FROM social_news.candidate candidate
WHERE content_row.raw_ref_table = 'social_news.candidate'
  AND content_row.raw_ref_id = candidate.id
  AND (
      COALESCE(candidate.is_representative, TRUE) = FALSE
      OR COALESCE(candidate.publish_status, candidate.status, '') IN ('DUPLICATE', 'DUPLICATE_SKIPPED', 'SKIPPED', 'TEXT_INVALID', 'ARCHIVED')
      OR COALESCE(candidate.status, '') IN ('DUPLICATE', 'DUPLICATE_SKIPPED', 'SKIPPED', 'TEXT_INVALID', 'ARCHIVED')
  );

UPDATE content.content_candidate content_row
SET facebook_post_id = COALESCE(NULLIF(content_row.facebook_post_id, ''), news.facebook_post_id),
    facebook_post_url = COALESCE(NULLIF(content_row.facebook_post_url, ''), news.facebook_post_url),
    published_at = COALESCE(content_row.published_at, news.published_at),
    updated_at = CURRENT_TIMESTAMP,
    content_updated_at = CURRENT_TIMESTAMP
FROM social_news.candidate news
WHERE content_row.raw_ref_table = 'social_news.candidate'
  AND content_row.raw_ref_id = news.id
  AND (
      COALESCE(content_row.facebook_post_url, '') = ''
      OR content_row.published_at IS NULL
      OR COALESCE(content_row.facebook_post_id, '') = ''
  )
  AND (
      COALESCE(news.facebook_post_url, '') <> ''
      OR news.published_at IS NOT NULL
      OR COALESCE(news.facebook_post_id, '') <> ''
  );
